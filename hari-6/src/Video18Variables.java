public class Video18Variables {
    public static void main(String[] args) {
        // learn variables with Tim Buchalka

        // variables = wadah untuk menyimpan nilai atau informasi.
        int myFirstNumber = (10 + 5) + (2 * 10);
        int mySecondNumber = 12;
        int myThirdNumber = myFirstNumber * 2;

        int myTotal = myFirstNumber + mySecondNumber + myThirdNumber;

        // challenge print (1000 - myTotal)

        int anotherTotal = 1000 - myTotal;
        System.out.println(anotherTotal);
    }
}
